// Default fallback for any unspecified countries
export const defaultNames = cricketPlayerNames.England;

// Function to get names based on country
export function getNamesForCountry(country) {
  return cricketPlayerNames[country] || cricketPlayerNames.England;
}

// Function to generate a random name based on country
export function generatePlayerName(country = 'England', userId = null, playerIndex = 0) {
  const countryNames = getNamesForCountry(country);
  const seededRandom = (seed) => {
    const x = Math.sin(seed) * 10000;
    return x - Math.floor(x);
  };

  const getRandomIndex = (array, baseSeed) => {
    if (userId) {
      const seed = parseInt(userId.replace(/[^0-9]/g, '').slice(-8) || '12345678', 10) + playerIndex + baseSeed;
      return Math.floor(seededRandom(seed) * array.length);
    }
    return Math.floor(Math.random() * array.length);
  };

  const firstNameIndex = getRandomIndex(countryNames.firstNames, 1);
  const lastNameIndex = getRandomIndex(countryNames.lastNames, 2);

  const firstName = countryNames.firstNames[firstNameIndex];
  const lastName = countryNames.lastNames[lastNameIndex];

  return `${firstName} ${lastName}`;
}

// Function to get all available countries
export function getAvailableCountries() {
  return Object.keys(cricketPlayerNames);
}

// Function to validate if a country has name data
export function isCountrySupported(country) {
  return cricketPlayerNames.hasOwnProperty(country);
}
